// Ініціалізуємо змінні
let y, x;

// Отримуємо значення x
x = parseInt(prompt("Введіть значення x:"));

// Записуєм значення для y та викликаємо функцію
y = 1 + x + chooseOption(x);

// Функція яка повертає вираз в залежності від значення x
