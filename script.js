   // Перший спосіб (використання лише команд розгалуження в скороченій формі)
   if (x <= 4) {
      return Math.exp(1) ** Math.log(2 + 2 * x) + 2 * x;
   }
   if (4 < x && x <= 7) {
      return 1 / Math.tan((1 + x) / 9) + 8 * x;
   }
   if (x > 7) {
      return 1 - 7 * x + x ** 2 - 2 * x ** 3;
   }
